
public class Arithmatic {

	public static void main(String[] args) {
		int a,b,r1,r2,r3,r4;
		a=20;
		b=10;
		r1= a+b;
		r2= a-b;
		r3= a*b;
		r4= a/b;
		System.out.println("Addition of 20 and 10 is : " + r1);
		System.out.println("Subtraction of 20 and 10 is : " + r2);
		System.out.println("Multiplication of 20 and 10 is : " + r3);
		System.out.println("Division of 20 and 10 is : " + r4);
	}

}
